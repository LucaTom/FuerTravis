
public class FuerTravis {
	
	public int addieren(int x, int y) {
		int ergebnis = x + y;
		return ergebnis;
	}
	
	public double dividieren(double x, double y) {
		double ergebnis = x / y;
		return ergebnis;
	}
	
	public boolean istGerade (int a) {
		if ( a % 2 == 0)
			return true;
		else 
			return false;
	}
	


}
